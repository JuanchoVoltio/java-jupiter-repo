public class Pista{
	public String nombre;
	public boolean chasis;
	public boolean motor;
	public boolean aerodinamica;
	
	public Pista(String nombre, boolean chasis, boolean motor, boolean aerodinamica) {
		this.nombre = nombre;
		this.chasis = chasis;
		this.motor = motor;
		this.aerodinamica = aerodinamica;
	}
}