public class Pista{
	public String nombre;
	public boolean chasis;
	public boolean motor;
	public boolean aerodinamica;
	public int vueltas;
	
	public Pista(String nombre, boolean chasis, boolean motor, boolean aerodinamica, int vueltas) {
		this.nombre = nombre;
		this.chasis = chasis;
		this.motor = motor;
		this.aerodinamica = aerodinamica;
		this.vueltas = vueltas;
	}
}